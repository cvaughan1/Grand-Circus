package LessonFolder.Arrays;

public class ArrayPractice5a {

public static void main(String[] args) {
 
	

	   
	        String[][] salutation = {
	            {"Mr. ", "Mrs. ", "Ms. "},
	            {"Kumar"}
	        };

	        // Mr. Kumar
	        System.out.println(salutation[0][0] + salutation[1][0]);

	        // Mrs. Kumar
	        System.out.println(salutation[0][1] + salutation[1][0]);
	    }
	}


